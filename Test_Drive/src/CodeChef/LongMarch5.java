public class LongMarch5
{
	public static void main(String[] args)
	{
		Scanner sc = new Scanner(System.in);
		int t, N;
		t = sc.nextInt();
		for(int l =0; l < t; l++)
		{
			N =sc.nextInt();
			int a[] = new int[N];
			for(int i=0; i < N; i++)
				a[i] = sc.nextInt();
		}
	}
}
